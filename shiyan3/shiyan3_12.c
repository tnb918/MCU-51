/*
f=12MHz，定时器/计数器T0，方式1，实现从P1.0口输出周期为（20+作业号58）ms的方波。【查询方式】
*/
#include <reg51.h>
sbit P1_0=P1^0;//定义变量P1_0为P1口最低位
void main() 
{
	TMOD=0x01;//写入方式控制字
	P1_0=0; //赋初值
	TH0=0x67;//写入计数初值
	TL0=0XA8;//12MHz 65536-39000=26536(ox67A8)
	TR0=1;//启动T0定时
	while(1)//死循环
	{
		if(TF0==1)//查询是否溢出
			{
				TF0=0;//溢出标志置零
				P1_0=!P1_0;//输出取反
				TH0=0x67;//重新写入计数初值
			  TL0=0XA8;
			}
	}
}